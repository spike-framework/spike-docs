    <div class="desc">

        <p>
            The Spike Script is the lightweight JavaScript transpiler offering object-oriented syntax and structure.
            It's primary language which Spike Core is developed in and also must be used in Spike projects as well.
            <br/>
            The advantage over another transpilers for ex. TypeScript, is that Spike Script has no any features which have bad influence on developer
            current knowledge about JavaScript, so every field, every function has exactly same syntax as in plain JavaScript object.
        </p>
        <p>
            These are the features which one Spike Script offers:
            <ul>
                <li>Packaging - every single file in project tree has package which is short of his location in project</li>
                <li>Imports - you can easy import any class using package and class name</li>
                <li>Classes - now defining, managing, creating instances, inheritance is so easy to write</li>
                <li>Class modificators - classes can be static, private or public (non declarative)</li>
                <li>Enums - use enum keyword to keep constants in one place</li>
                <li>Fields decorators - not affecting anything decorators over class fields helps to know what type is</li>
            </ul>
        </p>

        <p>
            In Spike, you can have <b>class</b> (public) which is used to create instances, you can also have <b>private class</b>
            which can be used only in certain file, also you can define <b>static class</b> which is singleton and can be accessed
            anywhere. <b>static class</b> can also be <b>private</b>.
        </p>

        <p>
            Spike <b>enum</b> is only keyword to help organize business login in project, because behind stage it is the
            same what <b>static class</b> is.
        </p>

